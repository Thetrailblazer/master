        <h3>Fly Gear</h3>
        <div class="text-left">
            An 8-10 wt. 10' fly rod and reel with a good drag system, and of course good travel case.
            <p>As far as lines go 150 yards of dacron 20lb backing in association with some fast sinking shooting head fly line should do the trick.
                For Leader, 20-25lb mono works great and for the tipit, 
                <a href="http://www.cabelas.com/product/fishing/fishing-line/leader-material%7C/pc/104793480/c/104719680/sc/104130180/cabela-s-single-strand-wire/710030.uts?destination=%2Fcatalog%2Fbrowse.cmd%3FN%3D1100389" target="_new">single Strand coil wire .016"(0.40mm)</a></p>
            <p>As far as flies go 50 clousers/deceivers 2/0 should do the trick. Mixed Colors with either a white, a black, or grey base are perfect choices.
                Some good combinations are: red/yellow/white, blue/white, green/white, chartreuse/white, plain black, purple/black, green/black, grey/black.
                I like thin gauged hooks, like gamagatzu b10s stinger to penetrate the tiger's jaws</p>

            <p>A Good pair of needle nose pliers with wire cutters.</p>
            <p><a href="http://www.amazon.com/gp/product/B009YSFU54?psc=1&redirect=true&ref_=oh_aui_detailpage_o05_s01" target="_new">
                Soft or hide Sided Tackle Bag</a> big enough for all your gear sans rod.</p>
            <p>And of course a <a href="http://www.amazon.com/gp/product/B00Y6NRZFU?psc=1&redirect=true&ref_=oh_aui_detailpage_o05_s00" target="_new">power adapter</a> for your phone and electronics.</p>
